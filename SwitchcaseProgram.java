package package2;

public class SwitchcaseProgram 
{
 public static void main(String[] args) 
 {
  switch(3)
  {
   case 1: System.out.println("Chrome Browser");
           break;
   case 2: System.out.println("Edge Browser");
           break;  
   case 3: System.out.println("Safari Browser");
           break;   
   default:System.out.println("Sorry your selection is wrong");
   System.out.println("Press 1 for Chrome Browser");
   System.out.println("Press 2 for Edge Browser");
   System.out.println("Press 3 for Safari Browser");
  }
}
}
